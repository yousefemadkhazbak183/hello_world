// Arithmetic Operators

fn main() {
    // Addition
    let x = 4;
    let v = 3;
    let c = x + v;
    println!("C = {}", c);
    // Subtraction
    let x = 4;
    let v = 3;

    let c = x - v;
    println!("C = {}", c);
    // Multiplication
    let x = 4;
    let v = 3;
    let c = x * v;
    println!("C = {}", c);
    // Division
    let x = 4;
    let v = 3;
    let c = x / v;
    println!("C = {}", c);
}
